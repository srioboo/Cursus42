int	ft_is_prime(int nb)
{
	int	i;
	int	div;

	div = 0;
	i = 1;
	if (nb == 0 || nb == 1)
		return (0);
	while (i <= nb)
	{
		if (nb % i == 0)
			div++;
		i++;
	}
	if (div > 2)
		return (0);
	return (1);
}
