#include <unistd.h>
#include <stdio.h>

void	ft_putnbr(int n)
{
	char	ascii;

	// n = n / 10;
	ascii = n/10 + '0';
	write(1, &ascii, 1);
	write(1, "\n", 1);
	write(1, "\n", 1);
}
